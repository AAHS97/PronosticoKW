    public void totaldeunidades(){
        if ((Integer)jSpincantModelo2.getValue() > 0){            
            js2 = (Integer)jSpincantModelo2.getValue();
        }
        if ((Integer)jSpinCantModelo3.getValue() > 0){
            js3 = (Integer)jSpinCantModelo3.getValue();
        }
        if ((Integer)jSpinCantModelo4.getValue() > 0){
            js4 = (Integer)jSpinCantModelo4.getValue();
        }
        if ((Integer)jSpincantModelo5.getValue() > 0){
            js5 = (Integer)jSpincantModelo5.getValue();
        }
        if ((Integer)jSpincantModelo6.getValue() > 0){
            js6 = (Integer)jSpincantModelo6.getValue();
        }
        if ((Integer)jSpincantModelo7.getValue() > 0){
            js7 = (Integer)jSpincantModelo7.getValue();
        }
        if ((Integer)jSpinCantModelo8.getValue() > 0){
            js8 = (Integer)jSpinCantModelo8.getValue();
        }
        int allunid = js2+js3+js4+js5+js6+js7+js8;
        jLunidad.setText(String.valueOf(allunid).toString());
        if (allunid > 100){
            JOptionPane.showMessageDialog(null, "Cantidad de Unidades Excedida....");
            jBreporte.setVisible(false);
        }else{
            jBreporte.setVisible(true);
        }
    }
    
