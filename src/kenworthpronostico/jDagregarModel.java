    private void jbAddModelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAddModelActionPerformed
        // TODO add your handling code here:
        int rvalue = allok();
        switch (rvalue){
            case 1: lbmensaje.setText("! Ingresar nombre del Modelo..."); break;
            case 2: lbmensaje.setText("! Ingresar tipo de Motor..."); break;
            case 3: lbmensaje.setText("! Ingresar tiempo de vestidura de Motor..."); break;
            case 4: lbmensaje.setText("! Seleccione la cantidad de Ejes..."); break;
            case 5: lbmensaje.setText("! Ingresar tiempo de vestidura de 2 Ejes..."); break;
            case 6: lbmensaje.setText("! Ingresar tiempo de vestidura de 3 Ejes..."); break;
            case 7: lbmensaje.setText("! El tiempo de vestidura no puede ser menor que 1..."); break;
            case 10: 
                lbmensaje.setText("! Procesando datos...");
                // funcion para agregar datos
                pr.addnewmodel(jTagModel.getText(), jTagMotor.getText(),
                                jTtiempoMot.getText(), jT2ejestiempo.getText(),
                                jT3ejestiempo.getText());
                lbmensaje.setText("! Datos agregados exitosamente..."); 
                break;
        }
       
    }//GEN-LAST:event_jbAddModelActionPerformed

