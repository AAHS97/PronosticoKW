    // Verifica si todos los campos estan llenos
    private Integer allok(){
        if (jTagModel.getText().isEmpty()) return 1;
        if (jTagMotor.getText().isEmpty()) return 2;
        if (jTtiempoMot.getText().isEmpty()) return 3;
        if (!jC2ejestiempo.isSelected() && !jC3ejestiempo.isSelected()){
            return 4;
        }else{
            if(jC2ejestiempo.isSelected()){
                if(jT2ejestiempo.getText().isEmpty()){
                    return 5;
                }else{
                    double value =  Double.parseDouble(jT2ejestiempo.getText());
                    if (value <1){
                        jT2ejestiempo.setText("");
                        return 7;
                    }
                }                
            }
            if(jC3ejestiempo.isSelected()){
                if(jT3ejestiempo.getText().isEmpty()){
                    return 6;
                }else{
                    double value =  Double.parseDouble(jT3ejestiempo.getText());
                    if (value <1){
                        jT3ejestiempo.setText("");
                        return 7;
                    }
                }         
            }
        }
        return 10;
    
    }
